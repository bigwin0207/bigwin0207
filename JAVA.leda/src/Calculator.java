public class Calculator {

    public static int sumTwoNumber0f(int first, int second){

        return first + second;
    }

    public static int minusTwoNumber(int num1, int num2){

        return num1 - num2;
    }

    public static int multiTwoNumber(int num3, int num4){

        return num3 * num4;
    }

    public static int divideTwoNumber(int num5, int num6){

        return num5 / num6;
    }

//
//
//             *   - checkMethod(): void 설명 : 함수 호출, 확인용 메소드
//             *   - sumTwoNumber(a:int, b:int): int 설명 : 두 수를 입력 받아 합을 리턴한다.
//            *   - minusTwoNumber(a:int, b:int): int 설명 : 두 수를 입력 받아 차를 리턴한다.
//            *   - multiTwoNumber(a:int, b:int): int 설명 : 두 수를 입력 받아 곱한 값을 리턴한다.
//             *   - divideTwoNumber(a:int, b:int): int 설명 : 두 수를 입력 받아 나눈 값을 리턴한다.
}
